package Btvn;

public class Model_tuoi {
  private int age;
  private int day;
  private int month;
public Model_tuoi(int age, int day, int month) {
	super();
	this.age = age;
	this.day = day;
	this.month = month;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public int getDay() {
	return day;
}
public void setDay(int day) {
	this.day = day;
}
public int getMonth() {
	return month;
}
public void setMonth(int month) {
	this.month = month;
}

}
